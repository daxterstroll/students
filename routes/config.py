"""
config.py
=========
Конфігурація застосунку.

SECRET_KEY використовується Flask для підпису сесійних cookie
(session). Якщо ключ витік або залишається однаковим "за замовчуванням"
у всіх інсталяціях (як було раніше - захардкоджений рядок прямо в коді
репозиторію), будь-хто, хто бачив цей код (включно з git-історією),
може підробити сесію будь-якого користувача, зокрема адміністратора.

Тепер ключ читається зі змінної середовища SECRET_KEY; якщо її не
задано, генерується випадковий ключ на час роботи процесу (це означає,
що після кожного перезапуску сервера всі активні сесії "зліт ають" -
користувачам доведеться залогінитись знову, але це набагато безпечніше,
ніж один статичний ключ, видимий у публічному коді).

Для продакшн-розгортання обов'язково задайте свій постійний ключ,
наприклад через змінну середовища:
    export SECRET_KEY="$(python -c 'import secrets; print(secrets.token_hex(32))')"
"""

import os
import secrets

SECRET_KEY = os.environ.get('SECRET_KEY') or secrets.token_hex(32)

if not os.environ.get('SECRET_KEY'):
    import logging
    logging.getLogger('Students').warning(
        "Змінна середовища SECRET_KEY не задана - згенеровано тимчасовий "
        "ключ для цього запуску. Усі сесії користувачів будуть скинуті "
        "при наступному перезапуску сервера. Задайте SECRET_KEY у "
        "середовищі для стабільної роботи в продакшні."
    )
